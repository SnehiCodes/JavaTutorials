public class wrapper{
    public static void main(String[] args){

        // wrapper provides a way to use primitive data types as reference data types.
        // wrapper is written with first letter capital and is spelled out completely.
        // For e.g. char = Character

        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.1416;
        String e = "Snehi";

        // They can still be used as a primitive data type.

        if (a==true)
        {
            System.out.println(e);
        }
    }
}